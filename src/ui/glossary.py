GLOSSARY_ORDER = list(GLOSSARY.keys())


def term_help(term):
    item = GLOSSARY.get(term)
    if not item:
        return "この用語の説明はまだ登録されていません。"
    return "%s 注意点: %s" % (item["plain"], item["watch"])


def term_rows(terms=None):
    selected = terms or GLOSSARY_ORDER
    return [
        {
            "用語": term,
            "ざっくり説明": GLOSSARY[term]["plain"],
            "注意点": GLOSSARY[term]["watch"],
        }
        for term in selected
        if term in GLOSSARY
    ]
